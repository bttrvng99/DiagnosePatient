    private void DefaultList(){
        PrescriptionDAO pd = new PrescriptionDAO();
        ArrayList<Prescription> pre_list = pd.getAllPrescription();
        Object[] columns = {"ID", "Tên", "Giá thành", "Thông tin"};
        Object[][] value = new String[pre_list.size()][4];
        for(int i=0 ;i<pre_list.size(); i++){
            value[i][0] = pre_list.get(i).getId()+"";
            value[i][1] = pre_list.get(i).getName();
            value[i][2] = pre_list.get(i).getPrice()+"";
            value[i][3] = pre_list.get(i).getInfo()+"";
            tbl1 = new DefaultTableModel(value, columns){
                @Override
		    public boolean isCellEditable(int row, int column) {
		       //unable to edit cells
                    return false;
        	}
            };
            jTable1.setModel(tbl1);
        }
    }

    private void FinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinishActionPerformed
        // TODO add your handling code here:
        if((!jTextArea2.getText().isEmpty())&&(jTable2.getRowCount()!=0)&&(!jTextField2.getText().isEmpty())){
            pd = new PrescriptionDAO();
            for(int i=0; i<jTable2.getModel().getRowCount(); i++){
                Prescription p1 = new Prescription();
                PrescriptionList p2 = new PrescriptionList();
                p1.setId(Integer.parseInt((String) jTable2.getModel().getValueAt(i, 0)));
                p1.setName(String.valueOf(jTable2.getModel().getValueAt(i, 1)));
                p1.setPrice(Float.parseFloat((String) jTable2.getModel().getValueAt(i, 2)));
                p1.setInfo((String) jTable2.getModel().getValueAt(i, 3));
                p2.setPrescription(p1);
                s_pre.add(p2);
            }
            d.setPre_list(s_pre);
            d.setResults(jTextArea1.getText()+"");
            d.setConclusion(jTextArea2.getText()+"");
            d.setStatus(jTextField2.getText()+"");
            java.util.Date date=new java.util.Date();  
            System.out.println(date);  
            d.setRegister_date(date);
            System.out.println(d.getPatient().getName());
            System.out.println(d.getTest_list().get(0).getTest().getName());
            dd = new DiagnosisDAO();
            dd.addDiagnosis(d);
            JOptionPane.showMessageDialog(this, "Đã thêm vào cơ sở dữ liệu");
            PatientFrm pf = new PatientFrm();
            pf.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Chưa nhập đầy đủ các ô trong cửa sổ này");
            return;
        }
    }//GEN-LAST:event_FinishActionPerformed

